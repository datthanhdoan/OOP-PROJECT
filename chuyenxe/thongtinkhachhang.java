package chuyenxe;

public class thongtinkhachhang {
    private String name;
    private int SDT;
    private String DiaChi;

    public thongtinkhachhang() {
        this.name = name;
        this.SDT = SDT;
        this.DiaChi = DiaChi;
    }

    public String getnamekh() {
        return name;
    }

    public void setnamekh(String name) {
        this.name = name;
    }

    public int getSDTkh() {
        return SDT;
    }

    public void setSDTkh(int SDT) {
        this.SDT = SDT;
    }

    public String getDiaChi() {
        return DiaChi;
    }

    public void setDiaChikh(String DiaChia) {
        this.DiaChi = DiaChia;
    }
}
